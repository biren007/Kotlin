fun main() {
    print("enter value : ")
    var a:Int=Integer.valueOf(readLine())
    
    println("int: "+a.toInt())
    println("byte: "+a.toByte())
    println("long: "+a.toLong())
    println("int: "+a.toFloat())
    println("double: "+a.toDouble())
    println("char: "+a.toChar())

}